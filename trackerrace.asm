# Car Dodge Game - Sprite ROJO completo + movimiento adelante/atrás
# SETUP:
#    Tools -> Bitmap Display:
#    Unit Width: 4, Unit Height: 4
#    Display Width: 256, Display Height: 512
#    Base: 0x10008000 ($gp)
#    Connect to MIPS
# 3. Tools -> Keyboard MMIO -> Connect to MIPS
# 4. Run -> Assemble (F3)
# 5. Run -> Go (F5)
#
# CONTROLES:
# A - Izquierda
# D - Derecha  
# W - Adelante (acelerar)
# S - Atrás (frenar)
# Q - Salir

.data
    display: .word 0x10008000
   
    # Colores del auto ROJO (tu sprite)
    colorBlack: .word 0x000000
    colorRed1: .word 0xFF0000      # Rojo brillante del cuerpo
    colorRed2: .word 0xB40000      # Rojo medio
    colorRed3: .word 0x8B0000      # Rojo oscuro
    colorGray1: .word 0x464646     # Gris ruedas
    colorGray2: .word 0x2E2E2E     # Gris oscuro
    colorBlue: .word 0x0000FF      # Azul para ventanas/detalles
    colorWhite: .word 0xFFFFFF
   
    carX: .word 28        # X del auto (centrado)
    carLane: .word 1      # 0=izq, 1=centro, 2=derecha
    carY: .word 50        # Y del auto
    carSpeed: .word 0     # Velocidad del auto (0-3)
   
    obsX: .word 28        # X del obstáculo
    obsY: .word 0         # Y inicial
    obsSpeed: .word 1     # Velocidad base del obstáculo
   
    score: .word 0
    lives: .word 3
   
    minX: .word 4
    maxX: .word 56
    minY: .word 10
    maxY: .word 58
    
    lane0X: .word 13      # X del carril izquierdo (más a la izquierda)
    lane1X: .word 32      # X del carril centro (centrado en pantalla)
    lane2X: .word 51      # X del carril derecho (más a la derecha)
    
    obsLane0X: .word 9    # Obstáculo en carril izq (ajustado)
    obsLane1X: .word 28   # Obstáculo en carril centro (sin cambios)
    obsLane2X: .word 47   # Obstáculo en carril derecho (ajustado)

.text
main:
    li $s0, 0x10008000
    
    # Inicializar carril en centro (carril 1)
    li $t0, 1
    sw $t0, carLane
    
    # Cargar posición correcta del carril centro
    lw $s1, lane1X
    sw $s1, carX          # Actualizar carX también
    
    lw $s2, carY
    lw $s3, obsX
    lw $s4, obsY
    lw $s5, carSpeed

loop:
    # 1. Actualizar scroll
    jal updateScroll
    
    # 2. Dibujar pista (borra fondo automáticamente)
    jal drawTrack
   
    # 3. Dibujar auto (con transparencia)
    move $a0, $s1
    move $a1, $s2
    jal drawCarSprite32
   
    # 4. Dibujar obstáculo
    move $a0, $s3
    move $a1, $s4
    jal drawObstacle
   
    # 5. Mover obstáculo
    lw $t8, carSpeed
    addi $t8, $t8, 2       # Base 2 + velocidad
    add $s4, $s4, $t8
    sw $s4, obsY           # ? AGREGAR ESTA LÍNEA (faltaba guardar!)
   
    # Reset obstáculo
    blt $s4, 128, noReset  # ? CAMBIAR: era 64, debe ser 128
    li $s4, -8             # ? CAMBIAR: era -5, mejor -8
    sw $s4, obsY           # ? AGREGAR: guardar después del reset
    
    lw $t0, score
    addi $t0, $t0, 1
    sw $t0, score
   
    # Cambiar carril del obstáculo
    lw $t1, score
    li $t2, 4
    div $t1, $t2
    mfhi $t1
   
    beq $t1, 0, setXFarLeft
    beq $t1, 1, setXLeft
    beq $t1, 2, setXRight
    li $s3, 50
    sw $s3, obsX           # ? AGREGAR: guardar X
    j noReset
   
setXFarLeft:
    lw $s3, obsLane0X     # En lugar de lw $s3, lane0X
    sw $s3, obsX
    j noReset
    
setXLeft:
    lw $s3, obsLane1X     # En lugar de lw $s3, lane1X
    sw $s3, obsX
    j noReset
    
setXRight:
    lw $s3, obsLane2X     # En lugar de lw $s3, lane2X
    sw $s3, obsX
    j noReset
   
noReset:
    # Check input
    li $t0, 0xffff0000
    lw $t1, 0($t0)
    andi $t1, $t1, 1
    beq $t1, 0, noKey
   
    lw $t1, 4($t0)
    beq $t1, 'a', moveLeft
    beq $t1, 'A', moveLeft
    beq $t1, 'd', moveRight
    beq $t1, 'D', moveRight
    beq $t1, 'w', moveUp
    beq $t1, 'W', moveUp
    beq $t1, 's', moveDown
    beq $t1, 'S', moveDown
    beq $t1, 'q', exit
    beq $t1, 'Q', exit
    j noKey
   
moveLeft:
    lw $t2, carLane
    beqz $t2, noKey          # Ya está en carril 0
    addi $t2, $t2, -1
    sw $t2, carLane
    
    # Actualizar posición X según carril
    beq $t2, 0, setLane0
    beq $t2, 1, setLane1
    j noKey

setLane0:
    lw $t3, lane0X
    sw $t3, carX
    move $s1, $t3
    j noKey
    
setLane1:
    lw $t3, lane1X
    sw $t3, carX
    move $s1, $t3
    j noKey
   
moveRight:
    lw $t2, carLane
    li $t3, 2
    bge $t2, $t3, noKey      # Ya está en carril 2
    addi $t2, $t2, 1
    sw $t2, carLane
    
    # Actualizar posición X según carril
    beq $t2, 1, setLane1Right
    beq $t2, 2, setLane2
    j noKey

setLane1Right:
    lw $t3, lane1X
    sw $t3, carX
    move $s1, $t3
    j noKey
    
setLane2:
    lw $t3, lane2X
    sw $t3, carX
    move $s1, $t3
    j noKey

moveUp:
    # Acelerar - solo aumenta velocidad, NO mueve el auto
    lw $t2, carSpeed
    li $t3, 3
    bge $t2, $t3, noKey    # Ya está al máximo
    addi $t2, $t2, 1
    sw $t2, carSpeed
    move $s5, $t2          # Actualizar $s5 también
    j noKey

checkSpeed:
    li $t3, 3
    bge $s5, $t3, noKey
    addi $s5, $s5, 1
    sw $s5, carSpeed
    j noKey

moveDown:
    # Frenar - solo disminuye velocidad, NO mueve el auto
    lw $t2, carSpeed
    blez $t2, noKey        # Ya está en 0
    addi $t2, $t2, -1
    sw $t2, carSpeed
    move $s5, $t2          # Actualizar $s5 también
    j noKey
    
checkSlowDown:
    blez $s5, noKey
    addi $s5, $s5, -1
    sw $s5, carSpeed
    j noKey

noKey:
    # ==========================================
    # DETECCIÓN DE COLISIÓN CORREGIDA
    # ==========================================
    # Auto: 32x32 píxeles, centrado en ($s1, $s2)
    # Obstáculo: 8x8 píxeles, esquina superior izq en ($s3, $s4)
    
    # Verificar si el obstáculo está en rango Y visible
    bltz $s4, noCol
    li $t0, 128
    bge $s4, $t0, noCol
    
    # Calcular bordes del auto (el sprite está centrado en $s1)
    move $t0, $s1
    addi $t0, $t0, -10        # Borde izquierdo (más tolerante)
    move $t1, $s1
    addi $t1, $t1, 10         # Borde derecho (más tolerante)
    move $t2, $s2
    addi $t2, $t2, 5          # Borde superior (más tolerante)
    move $t3, $s2
    addi $t3, $t3, 27         # Borde inferior (más tolerante)
    
    # Calcular bordes del obstáculo
    move $t4, $s3             # Borde izquierdo
    move $t5, $s3
    addi $t5, $t5, 8          # Borde derecho
    move $t6, $s4             # Borde superior
    move $t7, $s4
    addi $t7, $t7, 8          # Borde inferior
    
    # AABB Collision Detection
    # NO hay colisión si:
    bge $t0, $t5, noCol       # izq_auto >= der_obs
    ble $t1, $t4, noCol       # der_auto <= izq_obs
    bge $t2, $t7, noCol       # sup_auto >= inf_obs
    ble $t3, $t6, noCol       # inf_auto <= sup_obs
    
    # Si llegamos aquí, HAY COLISIÓN
    j collision

collision:
    # Restar una vida
    lw $t0, lives
    addi $t0, $t0, -1
    sw $t0, lives
   
    # Mostrar mensaje en consola
    li $v0, 4
    la $a0, hitMsg
    syscall
   
    li $v0, 1
    move $a0, $t0
    syscall
   
    li $v0, 4
    la $a0, newlineMsg
    syscall
   
    # ¿Game Over?
    beqz $t0, gameOver
   
    # Si quedan vidas, resetear obstáculo lejos
    li $s4, -20                    # Más lejos para dar tiempo
    sw $s4, obsY
    
    # Cambiar carril del obstáculo
    lw $t1, score
    li $t2, 4
    div $t1, $t2
    mfhi $t1
    
    beq $t1, 0, collSetXFarLeft
    beq $t1, 1, collSetXLeft
    beq $t1, 2, collSetXRight
    li $s3, 50
    sw $s3, obsX
    j noCol

collSetXFarLeft:
    lw $s3, obsLane0X     # Cambiar
    sw $s3, obsX
    j noCol
    
collSetXLeft:
    lw $s3, obsLane1X     # Cambiar
    sw $s3, obsX
    j noCol
    
collSetXRight:
    lw $s3, obsLane2X     # Cambiar
    sw $s3, obsX
    j noCol

gameOver:
    # Limpiar pantalla final
    jal clearScreen
    
    # Mostrar mensaje
    li $v0, 4
    la $a0, gameOverMsg
    syscall
   
    li $v0, 1
    lw $a0, score
    syscall

    li $v0, 4
    la $a0, newlineMsg
    syscall
    
    # Salir
    li $v0, 10
    syscall

noCol:
    # Delay optimizado
    li $t0, 8000           # Más corto = más frames = más suave
delayLoop:
    addi $t0, $t0, -1
    bgtz $t0, delayLoop
   
    j loop

exit:
    li $v0, 4
    la $a0, exitMsg
    syscall
    li $v0, 10
    syscall

fillScreen:
    move $t0, $a0
    move $t1, $a1
    li $t2, 8192
fillLoop:
    sw $t1, 0($t0)
    addi $t0, $t0, 4
    addi $t2, $t2, -1
    bgtz $t2, fillLoop
    jr $ra

clearScreen:
    addi $sp, $sp, -4
    sw $ra, 0($sp)
    
    li $a0, 0x10008000
    li $a1, 0x000000        # Negro
    jal fillScreen
    
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra

updateScroll:
    lw $t0, scrollOffset
    lw $t1, carSpeed
    
    # Scroll más suave
    addi $t0, $t0, 1        # Era 2, ahora 1 (más suave)
    
    # Agregar velocidad del auto
    add $t0, $t0, $t1
    
    # Loop cuando llega a 64
    li $t2, 64
loopCheck:
    blt $t0, $t2, saveScroll
    sub $t0, $t0, $t2
    j loopCheck
    
saveScroll:
    sw $t0, scrollOffset
    jr $ra


# REEMPLAZA COMPLETAMENTE drawTrack con esta versión:
drawTrack:
    addi $sp, $sp, -4
    sw $ra, 0($sp)
    
    lw $t9, scrollOffset    # Cargar offset actual
    
    # Sprite 1: Arriba (puede estar parcialmente fuera)
    li $a0, 0               # X = 0
    move $a1, $t9           # Y = offset
    addi $a1, $a1, -64      # Restar 64 para que empiece arriba
    jal drawPista64Simple
    
    # Sprite 2: Centro
    li $a0, 0
    move $a1, $t9           # Y = offset
    jal drawPista64Simple
    
    # Sprite 3: Abajo
    li $a0, 0
    move $a1, $t9
    addi $a1, $a1, 64       # Sumar 64 para que esté abajo
    jal drawPista64Simple
    
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra


drawPista64Simple:
    addi $sp, $sp, -24
    sw $ra, 0($sp)
    sw $s0, 4($sp)
    sw $s1, 8($sp)
    sw $s2, 12($sp)
    sw $s3, 16($sp)
    sw $s4, 20($sp)
   
    move $s0, $a0           # X inicial
    move $s1, $a1           # Y inicial
   
    la $s2, pista64_data
    li $s3, 0               # fila

rowLoop:
    add $s4, $s1, $s3       # Y_pantalla
    
    # Clipping vertical
    bltz $s4, skipRow
    li $t0, 128
    bge $s4, $t0, endSprite
    
    li $s5, 0               # columna

colLoop:
    lw $t0, 0($s2)          # RGBA
    srl $t1, $t0, 24        # Alpha
    
    # SI alpha = 0, dibujar NEGRO (esto borra el fondo)
    beqz $t1, drawBlack
    andi $t0, $t0, 0x00FFFFFF   # RGB
    j drawIt
    
drawBlack:
    li $t0, 0x000000        # Negro
    
drawIt:
    # Calcular dirección
    li $t2, 0x10008000
    move $t3, $s4
    li $t4, 64
    mult $t3, $t4
    mflo $t3
    add $t3, $t3, $s0
    add $t3, $t3, $s5
    sll $t3, $t3, 2
    add $t2, $t2, $t3
    sw $t0, 0($t2)          # Dibujar siempre

    addi $s2, $s2, 4
    addi $s5, $s5, 1
    li $t0, 64
    blt $s5, $t0, colLoop
    j nextRow

skipRow:
    addi $s2, $s2, 256      # Saltar fila

nextRow:
    addi $s3, $s3, 1
    li $t0, 64
    blt $s3, $t0, rowLoop

endSprite:
    lw $ra, 0($sp)
    lw $s0, 4($sp)
    lw $s1, 8($sp)
    lw $s2, 12($sp)
    lw $s3, 16($sp)
    lw $s4, 20($sp)
    addi $sp, $sp, 24
    jr $ra
   
