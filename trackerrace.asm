# Car Dodge Game - Sprite ROJO completo + movimiento adelante/atrįs
# SETUP:
#    Tools -> Bitmap Display:
#    Unit Width: 4, Unit Height: 4
#    Display Width: 256, Display Height: 512
#    Base: 0x10008000 ($gp)
#    Connect to MIPS
# 3. Tools -> Keyboard MMIO -> Connect to MIPS
# 4. Run -> Assemble (F3)
# 5. Run -> Go (F5)
#
# CONTROLES:
# A - Izquierda
# D - Derecha  
# W - Adelante (acelerar)
# S - Atrįs (frenar)
# Q - Salir

.data
    display: .word 0x10008000
   
    # Colores del auto ROJO (tu sprite)
    colorBlack: .word 0x000000
    colorRed1: .word 0xFF0000      # Rojo brillante del cuerpo
    colorRed2: .word 0xB40000      # Rojo medio
    colorRed3: .word 0x8B0000      # Rojo oscuro
    colorGray1: .word 0x464646     # Gris ruedas
    colorGray2: .word 0x2E2E2E     # Gris oscuro
    colorBlue: .word 0x0000FF      # Azul para ventanas/detalles
    colorWhite: .word 0xFFFFFF
   
    carX: .word 28        # X del auto (centrado)
    carY: .word 50        # Y del auto
    carSpeed: .word 0     # Velocidad del auto (0-3)
   
    obsX: .word 28        # X del obstįculo
    obsY: .word 0         # Y inicial
    obsSpeed: .word 1     # Velocidad base del obstįculo
   
    score: .word 0
    lives: .word 3
   
    minX: .word 4
    maxX: .word 56
    minY: .word 10
    maxY: .word 58

.text
main:
    li $s0, 0x10008000
    lw $s1, carX
    lw $s2, carY
    lw $s3, obsX
    lw $s4, obsY
    lw $s5, carSpeed

loop:

    jal updateScroll
    # LIMPIAR PANTALLA PRIMERO
    jal clearScreen
    
    # Dibujar pista (fondo)
    jal drawTrack
   
    # Dibujar auto ROJO completo
    move $a0, $s1
    move $a1, $s2
    jal drawCarSprite32
   
    # Dibujar obstįculo
    move $a0, $s3
    move $a1, $s4
    jal drawObstacle
   
    # Mover obstįculo (mįs rįpido)
    addi $s4, $s4, 2      # Cae 2 pixels por frame (era 1)
   
    # Reset obstįculo
    blt $s4, 64, noReset
    li $s4, -5
    lw $t0, score
    addi $t0, $t0, 1
    sw $t0, score
   
    # Cambiar carril del obstįculo
    lw $t1, score
    li $t2, 4
    div $t1, $t2
    mfhi $t1
   
    beq $t1, 0, setXFarLeft
    beq $t1, 1, setXLeft
    beq $t1, 2, setXRight
    li $s3, 50
    j noReset
   
setXFarLeft:
    li $s3, 10
    j noReset
setXLeft:
    li $s3, 22
    j noReset
setXRight:
    li $s3, 38
   
noReset:
    # Check input
    li $t0, 0xffff0000
    lw $t1, 0($t0)
    andi $t1, $t1, 1
    beq $t1, 0, noKey
   
    lw $t1, 4($t0)
    beq $t1, 'a', moveLeft
    beq $t1, 'A', moveLeft
    beq $t1, 'd', moveRight
    beq $t1, 'D', moveRight
    beq $t1, 'w', moveUp
    beq $t1, 'W', moveUp
    beq $t1, 's', moveDown
    beq $t1, 'S', moveDown
    beq $t1, 'q', exit
    beq $t1, 'Q', exit
    j noKey
   
moveLeft:
    lw $t2, minX
    ble $s1, $t2, noKey
    addi $s1, $s1, -2
    sw $s1, carX
    j noKey
   
moveRight:
    lw $t2, maxX
    bge $s1, $t2, noKey
    addi $s1, $s1, 2
    sw $s1, carX
    j noKey

moveUp:
    # Acelerar - el auto "sube" en pantalla y obstįculos van mįs rįpido
    lw $t2, minY
    ble $s2, $t2, checkSpeed
    addi $s2, $s2, -1
    sw $s2, carY
checkSpeed:
    li $t3, 3
    bge $s5, $t3, noKey
    addi $s5, $s5, 1
    sw $s5, carSpeed
    j noKey

moveDown:
    # Frenar - el auto "baja" y obstįculos van mįs lento
    lw $t2, maxY
    bge $s2, $t2, checkSlowDown
    addi $s2, $s2, 1
    sw $s2, carY
checkSlowDown:
    blez $s5, noKey
    addi $s5, $s5, -1
    sw $s5, carSpeed
    j noKey

noKey:
    # Check colisión
    sub $t0, $s1, $s3
    abs $t0, $t0
   
    li $t1, 8
    bgt $t0, $t1, noCol
   
    sub $t0, $s2, $s4
    abs $t0, $t0
    li $t1, 10
    bgt $t0, $t1, noCol
   
    j collision

collision:
    lw $t0, lives
    addi $t0, $t0, -1
    sw $t0, lives
   
    li $v0, 4
    la $a0, hitMsg
    syscall
   
    li $v0, 1
    move $a0, $t0
    syscall
   
    li $v0, 4
    la $a0, newlineMsg
    syscall
   
    beq $t0, $zero, gameOver
   
    li $s4, -5
    j noCol

gameOver:
    li $v0, 4
    la $a0, gameOverMsg
    syscall
   
    li $v0, 1
    lw $a0, score
    syscall

    li $v0, 4
    la $a0, newlineMsg
    syscall

    li $v0, 10
    syscall

noCol:
    # Delay
    li $t0, 50000
delayLoop:
    addi $t0, $t0, -1
    bgtz $t0, delayLoop
   
    j loop

exit:
    li $v0, 4
    la $a0, exitMsg
    syscall
    li $v0, 10
    syscall

fillScreen:
    move $t0, $a0
    move $t1, $a1
    li $t2, 8192
fillLoop:
    sw $t1, 0($t0)
    addi $t0, $t0, 4
    addi $t2, $t2, -1
    bgtz $t2, fillLoop
    jr $ra

clearScreen:
    addi $sp, $sp, -4
    sw $ra, 0($sp)
    
    li $a0, 0x10008000
    li $a1, 0x000000        # Negro
    jal fillScreen
    
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra

updateScroll:
    lw $t0, scrollOffset
    lw $t1, carSpeed
    
    # Scroll base (siempre se mueve)
    addi $t0, $t0, 1
    
    # Agregar velocidad del auto
    add $t0, $t0, $t1
    
    # Si llega a 64, volver a 0 (loop infinito)
    li $t2, 64
    blt $t0, $t2, saveScroll
    sub $t0, $t0, $t2
    
saveScroll:
    sw $t0, scrollOffset
    jr $ra


# REEMPLAZA COMPLETAMENTE drawTrack con esta versión:
drawTrack:
    addi $sp, $sp, -4
    sw $ra, 0($sp)
    
    lw $t9, scrollOffset    # Cargar offset actual
    
    # Sprite 1: Arriba (puede estar parcialmente fuera)
    li $a0, 0               # X = 0
    move $a1, $t9           # Y = offset
    addi $a1, $a1, -64      # Restar 64 para que empiece arriba
    jal drawPista64Simple
    
    # Sprite 2: Centro
    li $a0, 0
    move $a1, $t9           # Y = offset
    jal drawPista64Simple
    
    # Sprite 3: Abajo
    li $a0, 0
    move $a1, $t9
    addi $a1, $a1, 64       # Sumar 64 para que esté abajo
    jal drawPista64Simple
    
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra


# REEMPLAZA drawPista64 con esta versión SIMPLIFICADA:
drawPista64Simple:
    addi $sp, $sp, -24
    sw $ra, 0($sp)
    sw $s0, 4($sp)
    sw $s1, 8($sp)
    sw $s2, 12($sp)
    sw $s3, 16($sp)
    sw $s4, 20($sp)
   
    move $s0, $a0           # X inicial
    move $s1, $a1           # Y inicial (puede ser negativo)
   
    la $s2, pista64_data    # Puntero a datos del sprite
    li $s3, 0               # Contador de fila (0-63)

rowLoop:
    # Calcular Y real en pantalla
    add $s4, $s1, $s3       # Y_pantalla = Y_inicial + fila
    
    # æEstį esta fila visible? (0 <= Y < 128)
    bltz $s4, skipRow       # Si Y < 0, saltar
    li $t0, 128
    bge $s4, $t0, endSprite # Si Y >= 128, terminar
    
    # Esta fila SĶ es visible, dibujarla
    li $s5, 0               # Contador de columna (0-63)

colLoop:
    # Leer pixel del sprite
    lw $t0, 0($s2)          # RGBA
    srl $t1, $t0, 24        # Alpha
    beqz $t1, skipPixel     # Si alpha=0, skip
    
    # Calcular dirección en pantalla
    andi $t0, $t0, 0x00FFFFFF   # RGB solo
    li $t2, 0x10008000
    move $t3, $s4           # Y
    li $t4, 64
    mult $t3, $t4
    mflo $t3
    add $t3, $t3, $s0       # + X inicial
    add $t3, $t3, $s5       # + columna
    sll $t3, $t3, 2
    add $t2, $t2, $t3
    
    # Dibujar pixel
    sw $t0, 0($t2)

skipPixel:
    addi $s2, $s2, 4        # Siguiente pixel en datos
    addi $s5, $s5, 1        # Siguiente columna
    li $t0, 64
    blt $s5, $t0, colLoop   # Repetir 64 columnas
    
    j nextRow

skipRow:
    # Saltar 64 pixels en los datos (64*4 = 256 bytes)
    addi $s2, $s2, 256

nextRow:
    addi $s3, $s3, 1        # Siguiente fila
    li $t0, 64
    blt $s3, $t0, rowLoop   # Repetir 64 filas

endSprite:
    lw $ra, 0($sp)
    lw $s0, 4($sp)
    lw $s1, 8($sp)
    lw $s2, 12($sp)
    lw $s3, 16($sp)
    lw $s4, 20($sp)
    addi $sp, $sp, 24
    jr $ra
   
# Dibujar auto ROJO completo (basado en tu sprite)
# $a0 = x (centro), $a1 = y (arriba)
drawCarRed:
    addi $sp, $sp, -8
    sw $ra, 0($sp)
    sw $s6, 4($sp)
   
    move $s6, $a0
    move $s7, $a1
   
    # FILA 0-1: Aleta trasera superior (negro)
    move $a0, $s6
    move $a1, $s7
    li $a2, 0x000000
    jal setPixel
   
    # FILA 2: Inicio cockpit (negro + rojo)
    addi $a1, $s7, 2
    move $a0, $s6
    addi $a0, $a0, -1
    li $a2, 0x000000
    jal setPixel
   
    move $a0, $s6
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 1
    jal setPixel
   
    # FILA 3: Cockpit/ventana (rojo + azul)
    addi $a1, $s7, 3
    move $a0, $s6
    addi $a0, $a0, -2
    li $a2, 0xFF0000
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, -1
    li $a2, 0x0000FF
    jal setPixel
   
    move $a0, $s6
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 1
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 2
    li $a2, 0xFF0000
    jal setPixel
   
    # FILA 4-5: Cuerpo principal ROJO
    li $t8, 2
drawBodyMain:
    move $a1, $s7
    addi $a1, $a1, 4
    add $a1, $a1, $t8
   
    move $a0, $s6
    addi $a0, $a0, -2
    li $a2, 0xB40000
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, -1
    li $a2, 0xFF0000
    jal setPixel
   
    move $a0, $s6
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 1
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 2
    li $a2, 0xB40000
    jal setPixel
   
    addi $t8, $t8, -1
    bgtz $t8, drawBodyMain
   
    # FILA 6: Transición (gris + rojo)
    addi $a1, $s7, 6
    move $a0, $s6
    addi $a0, $a0, -3
    li $a2, 0x464646
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, -2
    li $a2, 0x2E2E2E
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, -1
    li $a2, 0xFF0000
    jal setPixel
   
    move $a0, $s6
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 1
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 2
    li $a2, 0x2E2E2E
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 3
    li $a2, 0x464646
    jal setPixel
   
    # FILA 7-9: Zona media (grises + rojo central)
    li $t8, 3
drawMidSection:
    move $a1, $s7
    addi $a1, $a1, 7
    add $a1, $a1, $t8
   
    move $a0, $s6
    addi $a0, $a0, -3
