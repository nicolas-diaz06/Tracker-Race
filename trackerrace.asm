    addi $a0, $a0, 1
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 2
    jal setPixel
   
    addi $t8, $t8, -1
    bgtz $t8, drawLowerSection
   
    # FILA 12-13: Aleta trasera inferior (azul)
    addi $a1, $s7, 12
    move $a0, $s6
    addi $a0, $a0, -1
    li $a2, 0x0000FF
    jal setPixel
   
    move $a0, $s6
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 1
    jal setPixel
   
    lw $ra, 0($sp)
    lw $s6, 4($sp)
    addi $sp, $sp, 8
    jr $ra
   
setPixel:
    li $t0, 0x10008000
    move $t1, $a1
    mul $t1, $t1, 64
    add $t1, $t1, $a0
    sll $t1, $t1, 2
    add $t0, $t0, $t1
    sw $a2, 0($t0)
    jr $ra
   
# Dibuja sprite de 32x32 del archivo animacion choque piedra.c
# a0 = x (posición X)
# a1 = y (posición Y)
drawCarSprite32:
    la   $t0, animacion_choque_piedra_data  # dirección base del sprite
    li   $t1, 32     # ancho
    li   $t2, 32     # alto
    li   $t3, 0      # fila = 0

loopY32:
    li   $t4, 0      # columna = 0
loopX32:
    lw   $t5, 0($t0)         # color RGBA
    srl $t7, $t5, 24          # extraer byte alpha
    beqz $t7, skipPixel32     # si alpha=0, skip (transparente)
    andi $t6, $t5, 0x00FFFFFF # sino, usar RGB
   
    # calcular dirección de pķxel
    li   $t7, 0x10008000
    add  $t8, $a1, $t3
    mul  $t8, $t8, 64         # ancho pantalla
    add  $t8, $t8, $a0
    add  $t8, $t8, $t4
    sll  $t8, $t8, 2
    add  $t7, $t7, $t8
    sw   $t6, 0($t7)

skipPixel32:
    addi $t0, $t0, 4
    addi $t4, $t4, 1
    blt  $t4, 32, loopX32

    addi $t3, $t3, 1
    blt  $t3, 32, loopY32

    jr $ra

drawObstacle:
    addi $sp, $sp, -8
    sw $ra, 0($sp)
    sw $s6, 4($sp)      # Guardar $s6
   
    move $s6, $a0       # Guardar X original
    move $s7, $a1       # Guardar Y original
   
    li $t5, 0           # Contador de filas (0 a 7)
    li $a2, 0xFFFF00    # Amarillo
   
drawObsYLoop:
    move $a0, $s6       # Restaurar X
    move $a1, $s7       # Y base
    add $a1, $a1, $t5   # Sumar fila actual
   
    li $t6, 0           # Contador de columnas (0 a 7)
drawObsXLoop:
    jal setPixel
    addi $a0, $a0, 1    # Siguiente columna
    addi $t6, $t6, 1
    li $t7, 8
    blt $t6, $t7, drawObsXLoop
   
    addi $t5, $t5, 1
    li $t7, 8
    blt $t5, $t7, drawObsYLoop
   
    lw $ra, 0($sp)
    lw $s6, 4($sp)
    addi $sp, $sp, 8
    jr $ra

