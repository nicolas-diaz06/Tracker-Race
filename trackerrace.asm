    addi $a0, $a0, 1
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 2
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, 3
    li $a2, 0x464646
    jal setPixel
   
    addi $t8, $t8, -1
    bgtz $t8, drawMidSection
   
    # FILA 10-11: Zona baja (rojo + azul detalles)
    li $t8, 2
drawLowerSection:
    move $a1, $s7
    addi $a1, $a1, 10
    add $a1, $a1, $t8
   
    move $a0, $s6
    addi $a0, $a0, -2
    li $a2, 0xFF0000
    jal setPixel
   
    move $a0, $s6
    addi $a0, $a0, -1
    jal setPixel
   
    move $a0, $s6
    jal setPixel
   
